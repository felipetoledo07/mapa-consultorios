function start() {
    let btns = document.querySelectorAll("button");
    
    for(let bt of btns) {
        bt.addEventListener('click', event => {
            change(bt);
        })
    }
    function changeCss(bt_par) {
        for(let bt of btns) {
            bt.style.backgroundColor = "#FFE2D7";
            bt.style.color = "#000000"
            bt.removeAttribute("id");
        }
        bt_par.style.backgroundColor = "#FF043C";
        bt_par.style.color = "#ffffff"
        bt_par.setAttribute("id", "selected")
    }
    
    function change(bt_par) {
        changeCss(bt_par);
        changeSchedule();
        getNames();
        calculatePercentage()
    }

    change(btns[0]);
    
}


function changeSchedule() {
    let times = document.querySelectorAll(".time");
    let room = document.getElementById("selected").innerHTML.slice(11);
    if(room=="") {
        room = 13
    } else {
        room=Number(room);
    }
    times.forEach(function(time) {
        let time_clock = time.parentElement.firstElementChild.innerHTML + ":00";
        if(time.classList.contains("segunda")) {
            time.innerHTML = data[room-1][0][time_clock]
        } else if (time.classList.contains("terca")) {
            time.innerHTML = data[room-1][1][time_clock]
        } else if (time.classList.contains("quarta")) {
            time.innerHTML = data[room-1][2][time_clock]
        } else if (time.classList.contains("quinta")) {
            time.innerHTML = data[room-1][3][time_clock]
        } else if (time.classList.contains("sexta")) {
            time.innerHTML = data[room-1][4][time_clock]
        } else if (time.classList.contains("sabado")) {
            time.innerHTML = data[room-1][5][time_clock]
        } else if (time.classList.contains("domingo")) {
            time.innerHTML = data[room-1][6][time_clock]
        }  
    })
}

function getNames() {
    let spaces = document.querySelectorAll(".time");
    let names = new Set();
    spaces.forEach(function(space) {
        if(space.innerHTML != "livre" && space.innerHTML != "Descanso Semanal") {
            names.add(space.innerHTML);
        }
        space.style.backgroundColor = "#ffffff"
        if(space.innerHTML == "livre") {
          space.style.backgroundColor = "#d7f5df"
          console.log("teste");
        }
    })
    
    let text = "";
    let doctor_name = document.getElementById("doctor_name")
    names = Array.from(names).sort();
    names.forEach(function(name) {
        text += name + " / ";
    })
    text = text.slice(0,-3)
    doctor_name.innerHTML = text;
}

function calculatePercentage() {
    let spaces = document.querySelectorAll(".time")
    let counter = 0;
    spaces.forEach(function(space) {
        if(space.innerHTML == "livre") {
            counter += 1;
        }
    })
    let desocupation = document.getElementById("desocupation")
    desocupation.innerHTML = "Taxa de desocupação: " + counter / 2 + " %"
    let ocupation = document.getElementById("ocupation")
    ocupation.innerHTML = "Taxa de ocupação: " + (200 - counter) / 2 + " %"
}

